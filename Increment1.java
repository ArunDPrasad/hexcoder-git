public class Increment1{
	public static void main(String args[]){
		
		// x++ : Post Increment
		// ++x : Pre Increment
		
		int x = 5;
		
		System.out.println(x);//5
		System.out.println(++x);//6
		System.out.println(x);
		
		System.out.println();
		
		int y = 2;
		System.out.println(y);//2
		System.out.println(y++);//2
		System.out.println(y);//3
		

		
		
		
		
	}
}